package day2;
public class Conversion2 {
	public static void main(String[] args) {
	double d=258.78;
	int i=35;
	System.out.println("d="+d);
	System.out.println("i="+i);
	System.out.println("After type conversion");
	i=(int)d;
	System.out.println("d="+d);
	System.out.println("i="+i);
	}
}
