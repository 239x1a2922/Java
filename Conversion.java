package day2;
public class Conversion {
	public static void main(String[] args) {
		int i=29;
		double d=258.78;
		System.out.println("i="+i);
		System.out.println("d="+d);
		System.out.println("After primitive type conversion");
		d=i;
		System.out.println("i="+i);
		System.out.println("d="+d);
	}
}
